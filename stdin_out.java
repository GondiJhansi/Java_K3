import java.util.*;

public class Stdin_Out 
{
	public static void main(String[] args) 
	{
        	Scanner scan = new Scanner(System.in);
		System.out.println("Enter numbers:");
        	int a = scan.nextInt();
        	int b = scan.nextInt();
        	int c = scan.nextInt();
        	System.out.println(a);
        	System.out.println(b);
        	System.out.println(c);
    	}
}
